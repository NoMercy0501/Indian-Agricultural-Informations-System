 <p>Biofertilisers are good source for enhancing the nutrient availability in soil and plants. These are categorized on basis of the specific nutrient availability concern. Nitrogenous Biofertilisers are capable of fixing atmospheric nitrogen when suitable crops are inoculated with them. Biofertilisers are low cost, effective,environmental&nbsp;friendly and renewable source of plant nutrients to supplement&nbsp;fertilisers. Integration of chemical, organic and biological sources of plant nutrients and their management is necessary for maintaining soil health for sustainable agriculture. The bacterial organisms present in the&nbsp;biofertiliser&nbsp;either fix atmospheric nitrogen or&nbsp;solubilise&nbsp;insoluble forms of soil nutrients. IFFCO is producing nitrogen fixing biofertilisers (Rhizobium, Azotobacter, Acetobacter),Phosphate Solubilising Bacteria (PSB) for phosphorus, Potassium Mobilizing Biofertiliser (KMB) for potassium, Zinc Solubilizing Biofertiliser (ZSB) for zinc and NPK liquid consortia for nitrogen, phosphorus &amp; potassium. To cater the growing demand of liquid biofertilisers, IFFCO has focus to produce liquid biofertilisers instead of solid carrier based. The main advantages of liquid biofertiliser are higher efficiency, easier application &amp; handling, and better shelf life.</p>
